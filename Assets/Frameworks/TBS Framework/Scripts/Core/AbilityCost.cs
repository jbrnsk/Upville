/// <summary>
/// Class representing the costs associated with an ability.
/// </summary>
public class AbilityCost
{
    /// <summary>
    /// Strength cost of ability
    /// </summary>
    public int StrengthCost { get; set; }

    /// <summary>
    /// Strength cost of ability
    /// </summary>
    public int SpeedCost { get; set; }

    /// <summary>
    /// Strength cost of ability
    /// </summary>
    public int CunningCost { get; set; }
}